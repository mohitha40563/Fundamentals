package Fundamentals;

public class Alphabetic {
	public static void main(String args[])
	{
	    char a=args[0].charAt(0);
	    char b=args[1].charAt(0);
	    int n=a,s=b;
	    
	    if(n>=s)
	    	System.out.println(b+","+a);
	    else
	    	System.out.println(a+","+b);
	    
		
		
		
	}
}
